        fun process(objects: Array<Any>): Array<Item> {
            return Arrays.stream<Any>(objects).map<Item?> { `object`: Any -> process(`object`) }
                .filter { obj: Item? -> Objects.nonNull(obj) }.toArray<Item> { _Dummy_.__Array__() }
        }

        fun process(`object`: Any): Item? {
            return if (`object` is RegistrySupplier<*>) {
                if (`object`.get() is ItemLike) item.asItem() else null
            } else if (`object` is ItemLike) `object`.asItem() else null
        }
    }
