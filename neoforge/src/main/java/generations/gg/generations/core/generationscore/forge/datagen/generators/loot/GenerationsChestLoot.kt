    private fun registerBallLootPoolUniform(
        output: BiConsumer<ResourceKey<LootTable>, LootTable.Builder>,
        ball: ResourceKey<LootTable>,
        min: Float,
        max: Float,
        vararg items: Any
    ) {
        registerBallLootPool(output, ball, UniformGenerator.between(min, max), *items)
    }

    private fun registerBallLootPoolConstant(
        output: BiConsumer<ResourceKey<LootTable>, LootTable.Builder>,
        ball: ResourceKey<LootTable>,
        constant: Float,
        vararg items: Any
    ) {
        registerBallLootPool(output, ball, ConstantValue.exactly(constant), *items)
    }


    private fun registerBallLootPool(
        output: BiConsumer<ResourceKey<LootTable>, LootTable.Builder>,
        ball: ResourceKey<LootTable>,
        provider: NumberProvider,
        vararg items: Any
    ) {
        output.accept(ball, LootTable.lootTable().withPool(createPool(provider, *process(items))))
    }

    private fun createPool(rolls: NumberProvider, vararg items: Item): LootPool.Builder {
        val pool = LootPool.lootPool().setRolls(rolls)
        for (item in items) pool.add(LootItem.lootTableItem(item))

        return pool
    }

